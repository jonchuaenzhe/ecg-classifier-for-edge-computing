    int comparator_out;
    int ALU_out;

    // Multiplication calculation
    mul_result_0 = weights[0] * 256;
    mul_result_1 = (MUL_enable >= 1) ? weights[1] * data[0] : 0;
    mul_result_2 = (MUL_enable >= 2) ? weights[2] * data[1] : 0;
